    private void jtfPesquisaProdutoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPesquisaProdutoKeyPressed
        if (jtfPesquisaProduto.getText() != null && jtfPesquisaProduto.getText().length() > 3){
            System.out.println("teste"+jtfPesquisaProduto.getText());
            
            ArrayList<Produto> pesquisaProdutoPorTexto = produtoControler.pesquisaPorTexto(jtfPesquisaProduto.getText());
            if (pesquisaProdutoPorTexto != null && !pesquisaProdutoPorTexto.isEmpty()){
                DefaultTableModel model = (DefaultTableModel) jtbPesquisaProduto.getModel();
                model.setNumRows(0);
                SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
                for (Produto produto : pesquisaProdutoPorTexto) {
                    model.addRow(new Object[]{
                        produto.getId(),
                        produto.getNome(),
                        produto.getDescricao(),
                        produto.getPreco_unitario(),
                        produto.getUnidade(),
                        produto.getTipoproduto(),
                        produto.isRegistro_ativo(),
                        sdf.format(produto.getDataRegistro())
                    });
                }
            }
        }    
    }//GEN-LAST:event_jtfPesquisaProdutoKeyPressed

